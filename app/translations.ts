export const t = (key: string, lang: Language = 'en', replacements?: Record<string, string>): string => {
  let text = TRANSLATIONS[lang]?.[key] || TRANSLATIONS['en'][key] || key;
  if (replacements) {
    Object.entries(replacements).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
};
